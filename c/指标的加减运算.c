#include <stdio.h>

int main () {
    int v[5] = {9, 2, 6, 1, 3};

    // 指标对整数的加减运算
    int *a = &v[0] + 1; // == &v[1]（指标）
    int *b = &v[4] - 2; // == &v[2]（指标）
    printf("%d\n", *a);
    printf("%d\n", *b);

    // 指标对指标的加减运算
    // &v[0] + &v[1] 编译失败，指标对指标没有加法
    int c = &v[4] - &v[1]; // == 3，得到的是两个元素之间间隔的长度（整数）
    printf("%d\n", c);

    return 0;
}