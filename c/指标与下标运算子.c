#include <stdio.h>

int main () {
    // 下标运算：a[b] == *(a+b)，括号里是最简单的数值运算，例如 v[1] == *(v+1) == *(&v[0]+1) == *&v[1] == v[1]
    // 1[v] == *(1+v) == *(1+&v[0]) == v[1]
    int v[5] = {9, 2, 6, 1, 3};
    int *n = v;
    // 此时，n[0] == *(n+0) == *(&v[0]+0) == v[0]，故可以用 n[i] 代替 v[i]，这相当于一次隐性转型（指标 -> 阵列）
    printf("%d\n", n[2]);
    return 0;
}